package Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float s=1.23334f;
        System.out.printf("formatted output is %.2f",s);
        //% act as placeholder.
        String name="rahul";
        int age=22;
        System.out.printf("my name is %s and i am %d",name,age);
    }
}
